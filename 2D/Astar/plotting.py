

import os
import sys
import matplotlib.pyplot as plt

import Astar.env as env


class Plotting:
    def __init__(self, xI, xG,alg_env):
        self.xI, self.xG = xI, xG
        self.env = alg_env #env.Env(self.xI,self.xG)
        self.obs = self.env.obs
        self.name = None
        self.time = 0

    def update_obs(self, obs):
        self.obs = obs

    def animation(self, path, visited, name,time):
        self.name = name
        self.time = time
        self.plot_grid(name)
        self.plot_visited(visited)
        self.plot_path(path)
        plt.show()



    def plot_grid(self, name):
        obs_x = [x[0] for x in self.obs]
        obs_y = [x[1] for x in self.obs]

        plt.plot(self.xI[0], self.xI[1], "bs")
        plt.plot(self.xG[0], self.xG[1], "gs")
        plt.plot(obs_x, obs_y, "sk")
        plt.title(name)
        plt.axis("equal")

    def plot_visited(self, visited, cl='gray'):
        if self.xI in visited:
            visited.remove(self.xI)

        if self.xG in visited:
            visited.remove(self.xG)

        count = 0

        for x in visited:
            count += 1
            plt.plot(x[0], x[1], color="blue",alpha=0.1, marker='o')
            plt.gcf().canvas.mpl_connect('key_release_event',
                                         lambda event: [exit(0) if event.key == 'escape' else None])

            if count < len(visited) / 3:
                length = 20
            elif count < len(visited) * 2 / 3:
                length = 30
            else:
                length = 40
            #
            # length = 15

            if count % length == 0:
                plt.pause(0.001)
        plt.pause(0.01)

    def plot_path(self, path, cl='r', flag=False):
        
        path_x = [path[i][0] for i in range(len(path))]
        path_y = [path[i][1] for i in range(len(path))]
         
        if not flag:  
            plt.plot(path_x, path_y, linewidth='1', color='r')
        else:
            plt.plot(path_x, path_y, linewidth='3', color=cl)

        plt.plot(self.xI[0], self.xI[1], "bs")
        plt.plot(self.xG[0], self.xG[1], "gs")
        #plt.text(30, 30, 'Path Length' + str(len(path)), fontsize = 10)
        plt.title(self.name + '\nPath Length = ' + str(len(path)) + '\n Path Computing Time = ' + str(self.time))
        plt.pause(0.01)

